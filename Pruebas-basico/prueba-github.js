// Archivo de prueba para funciones de GitHub en Node.js

function multiplicar(a, b) {
    return a * b;
}

function dividir(a, b) {
    if (b === 0) return 'No se puede dividir entre cero';
    return a / b;
}

console.log('Multiplicación 4*5 =', multiplicar(4, 5));
console.log('División 10/2 =', dividir(10, 2));

